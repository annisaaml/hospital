    private void tabelPasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPasienMouseClicked
        // TODO add your handling code here:
        int baris = tabelPasien.rowAtPoint(evt.getPoint());
        String kd_pasien = tabelPasien.getValueAt(baris, 0).toString();
        txtKd_pasien.setText(kd_pasien);

        String nama = tabelPasien.getValueAt(baris, 1).toString();
        txtNama.setText(nama);

        String jk = tabelPasien.getValueAt(baris, 2).toString();
        cbJK.setSelectedItem(jk);

        String berat_badan = tabelPasien.getValueAt(baris, 3).toString();
        txtBB.setText(berat_badan);

        String tinggi_badan = tabelPasien.getValueAt(baris, 4).toString();
        txtTB.setText(tinggi_badan);

        String golongan_darah = tabelPasien.getValueAt(baris, 5).toString();
        cbGoldar.setSelectedItem(golongan_darah);

        String alamat = tabelPasien.getValueAt(baris, 6).toString();
        txtAlamat.setText(alamat);
    }//GEN-LAST:event_tabelPasienMouseClicked

