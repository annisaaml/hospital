    private void cariData(String key){
    ResultSet RsPasien=null;
        try{
            Object[] judul_kolom = {"Kode Pasien", "Nama", "Jenis Kelamin", "Berat Badan (Kg)", "Tinggi Badan (cm)", "Gol. Darah", "Alamat"};
            model=new DefaultTableModel(null,judul_kolom);
            tabelPasien.setModel(model);
            
            Connection conn=(Connection)TubesSMT4.configDB();
            Statement stt=conn.createStatement();
            model.getDataVector().removeAllElements();
            
            RsPasien=stt.executeQuery("SELECT * from pasien WHERE kd_pasien LIKE '%"+key+"%' OR nama LIKE '%"+key+"%' OR alamat LIKE '%"+key+"%'");
            while(RsPasien.next()){
                Object[] data={
                    RsPasien.getString(1),
                    RsPasien.getString(2),
                    RsPasien.getString(3),
                    RsPasien.getString(4),
                    RsPasien.getString(5),
                    RsPasien.getString(6),
                    RsPasien.getString(7),
                };
               model.addRow(data);
            }                
        } catch (SQLException ex) {
            Logger.getLogger(Pasien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void tampilkan_data(){
        
        int row = tabelPasien.getRowCount();
        for(int a = 0;a<row;a++){
            model.removeRow(0);
        }
        try {
            Connection conn = TubesSMT4.configDB();
            ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM pasien");
            while(rs.next()){
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),
                rs.getString(6),rs.getString(7)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pasien.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }

