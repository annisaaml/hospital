private void cariData(String key){
    ResultSet RsObat=null;
        try{
            Object[] judul_kolom = {"Kode Obat", "Nama Obat", "Harga"};
            model=new DefaultTableModel(null,judul_kolom);
            tabelObat.setModel(model);
            
            Connection conn=(Connection)TubesSMT4.configDB();
            Statement stt=conn.createStatement();
            model.getDataVector().removeAllElements();
            
            RsObat=stt.executeQuery("SELECT * from obat WHERE kd_obat LIKE '%"+key+"%'OR nama_obat LIKE '%"+key+"%'");  
            while(RsObat.next()){
                Object[] data={
                    RsObat.getString(1),
                    RsObat.getString(2),
                    RsObat.getString(3),       
                };
               model.addRow(data);
            }                
        } catch (SQLException ex) {
            Logger.getLogger(Obat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void tampilkan_data(){
        
        int row = tabelObat.getRowCount();
        for(int a = 0;a<row;a++){
            model.removeRow(0);
        }
        try {
            Connection conn = TubesSMT4.configDB();
            ResultSet rs = conn.createStatement().executeQuery("SELECT *FROM obat");
            while(rs.next()){
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Obat.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }
