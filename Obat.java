    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn =  TubesSMT4.configDB();
            conn.createStatement().executeUpdate ("UPDATE obat SET kd_obat = '" +txtKd_obat.getText()+ "'," + "nama_obat= '" +txtNama.getText()+ "'," + "harga_obat= '" +txtHarga.getText()+ "'" + "WHERE kd_obat = '" +txtKd_obat.getText()+"'");
            JOptionPane.showMessageDialog(null, "Selamat proses edit data berhasil!");
            tampilkan_data();
            kosongkan_form();

        }catch(HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Error","Pesan Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editActionPerformed

